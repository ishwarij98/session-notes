import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true
    },
    password: {
        type: String,
        required: true,
        minlength: 6
    }
},
//T imestamps will add createdAt and updatedAt.
    { timestamps: true });

    const userModel = mongoose.model("User", userSchema);
    export default userModel;